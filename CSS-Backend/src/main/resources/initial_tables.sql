CREATE TABLE DOCTOR (
    DOCTOR_ID NUMBER NOT NULL,
    USER_ID INT NOT NULL,
    DOCTOR_HOSPITAL INT NOT NULL, -- Foreign key to a Hospital table
    DOCTOR_DEPARTMENT INT NOT NULL, -- Foreign key to a Department table
    DOCTOR_SPECIALTY INT NOT NULL, -- Foreign key to a Specialty table
    DOCTOR_E_SIGNATURE BLOB,
    DOCTOR_LICENSE_NUMBER VARCHAR2(8) NOT NULL,
    DOCTOR_LICENSE_EXP_DATE DATE NOT NULL,
